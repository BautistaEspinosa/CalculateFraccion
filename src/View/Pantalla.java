  private int mcd(int num, int den) {
        int u = Math.abs(num);  
     int v = Math.abs(den);
     if(v == 0){
          return u;
     }
     int r;
     while(v != 0){
          r = u % v;
          u = v;
          v = r;
     }
     return u;
    }
  
  private int mcd1(int a, int b) {
        while (b != 0) {
            int temp = b;
            b = a % b;
            a = temp;
        }
        return a;
    }
